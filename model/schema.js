import mongoose from "mongoose";

const registerUser = new mongoose.Schema({
  userName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  confirmPassword: {
    type: String,
    required: true,
  },
});

const registerUserTemplate = mongoose.model("register", registerUser);

export default registerUserTemplate;
